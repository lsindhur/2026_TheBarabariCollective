//array methods are built in functions that are 
//available for all array objects

//these methods help you manipulate, access or transform the data inside the array
const arr = [1,2,3]

//inserting elements at the end of the array
//push
arr.push(4);
console.log(arr);



//Inserts new elements at the beginning of the array.
//unshift
arr.unshift(0)
console.log(arr);

//delete the last element
//pop
arr.pop()
console.log(arr)


//delete the first element
//shift
arr.shift();
console.log(arr)
